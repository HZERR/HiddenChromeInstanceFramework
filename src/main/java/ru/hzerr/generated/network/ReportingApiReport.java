package ru.hzerr.generated.network;

import ru.hzerr.annotation.Experimental;
import ru.hzerr.annotation.Description;
import ru.hzerr.annotation.Required;
import ru.hzerr.annotation.TypeExperimental;
import ru.hzerr.annotation.TypeDescription;

@Experimental
@Description("An object representing a report generated by the Reporting API.")
public class ReportingApiReport {

	@Required
	@TypeExperimental
	private String id;

	@Required
	@Description("The URL of the document that triggered the report.")
	private String initiatorUrl;

	@Required
	@Description("The name of the endpoint group that should be used to deliver the report.")
	private String destination;

	@Required
	@Description("The type of the report (specifies the set of data that is contained in the report body).")
	private String type;

	@Required
	@Description("When the report was generated.")
	@TypeDescription("UTC time in seconds, counted from January 1, 1970.")
	private Number timestamp;

	@Required
	@Description("How many uploads deep the related request was.")
	private int depth;

	@Required
	@Description("The number of delivery attempts made so far, not including an active attempt.")
	private int completedAttempts;

	@Required
	private Object body;

	@Required
	@TypeExperimental
	@TypeDescription("The status of a Reporting API report.")
	private String status;

    public ReportingApiReport() {
    }

	public String getId() {
		return this.id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getInitiatorUrl() {
		return this.initiatorUrl;
	}
	public void setInitiatorUrl(String initiatorUrl) {
		this.initiatorUrl = initiatorUrl;
	}
	public String getDestination() {
		return this.destination;
	}
	public void setDestination(String destination) {
		this.destination = destination;
	}
	public String getType() {
		return this.type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public Number getTimestamp() {
		return this.timestamp;
	}
	public void setTimestamp(Number timestamp) {
		this.timestamp = timestamp;
	}
	public int getDepth() {
		return this.depth;
	}
	public void setDepth(int depth) {
		this.depth = depth;
	}
	public int getCompletedAttempts() {
		return this.completedAttempts;
	}
	public void setCompletedAttempts(int completedAttempts) {
		this.completedAttempts = completedAttempts;
	}
	public Object getBody() {
		return this.body;
	}
	public void setBody(Object body) {
		this.body = body;
	}
	public String getStatus() {
		return this.status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
}
